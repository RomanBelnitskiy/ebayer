CREATE TABLE IF NOT EXISTS items_for_search (
    item_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	item_type varchar(8) NOT NULL,
	item_size int NOT NULL CHECK (item_size in (2,4,8,16,32,64)),
	item_formfactor varchar(10) NOT NULL,
	item_frequency varchar(6) NOT NULL,
	item_model varchar(100) NOT NULL,
	item_low_price double NOT NULL DEFAULT 1.0,
	item_high_price double NOT NULL DEFAULT 1.0,
	item_voltage double DEFAULT 0.0,
	item_comment varchar(255) DEFAULT ''
);